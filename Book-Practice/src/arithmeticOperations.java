//This program demonstrates arithmetic operations.

public class arithmeticOperations {
    public static void main(String[] args) {
        int a; //defines an integer variable named a
        int b; //defines an integer variable named b

        a = 3; //assigns value to the variable a
        b = 2; //assigns value to the variable b

        /*the following lines call for an output using the print line function
        and demonstrates various arithmetic operations on predefined variable*/
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
