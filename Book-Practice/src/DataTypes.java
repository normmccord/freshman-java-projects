//This program demonstrates variables, literal constants, and data types.

public class DataTypes { //begins DataTypes program
    public static void main(String[] args) {
        int i; //defines an integer variable named i
        double d; //defines a double variable named d
        String s; //defines a string variable named s
        boolean b; //defines a boolean variable named b

        //next 4 lines assign value to the variable
        i = 1234567890;
        d = 1.23456789012345;
        s = "string";
        b = true;

        //next 4 lines call for output with the print line function
        System.out.println("Integer i = " + i);
        System.out.println("Double d = " + d);
        System.out.println("String s = " + s);
        System.out.println("Boolean b = " + b);
    }
}
